# Realizar un programa que permita obtener la suma, resta, multiplicación y división de dos números x e y ingresados por teclado. El programa debe imprimir los números pedidos y el resultado de las cuatro operaciones.  

x = int(input("Ingrese un número por teclado: "))
y = int(input("Ingrese un número por teclado: "))

print("Números ingresados: x =", x, ", y =", y)

print("Suma: x + y =", x + y )

print("Resta: x - y =", x - y)

print("Multiplicación: x * y =", x * y)

print("División: x / y =", x / y)